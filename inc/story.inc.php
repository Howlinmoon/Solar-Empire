<p>In the year 2092, one hundred and twenty five years after the momentus
Apollo 11 mission to the moon, the four-member crew of the Starship Excelsior
became the first the first humans to travel to another star system. The
celebrations and excitement on Earth were intense, to say the least. Wild
parties were thrown which taxed the resources of police departments around
the world, the media was caught up in a frenzied coverage of the event,
politicians praised their own efforts and Jonathon Browning, a nondescript
technician at the sprawling SETI (Search for Extraterrestrial Intelligence)
Institute in Arizona received the first communication from an alien species.</p>
<p>The United Nations was aghast at the message. Humanity shared a collective
sense of rage and indignation. Far from the over-popularised demand of
&quot;Take me to your leader&quot;, the message was a polite but obviously
threatening warning, advising mankind never to leave the Solar System again or
risk war with a technologically advanced alien species. After months of heated
debate and political manoeuvring, the reply was broadcast into deep space, a
challenging request that the aliens mind their own business and steer clear of
our expansion into the galaxy.</p>
<p>The response of the aliens was swift and horribly brutal. A fleet of
streamlined starships began to orbit the Earth a few weeks later.  Without
provocation or a hint of warning, they proceeded to pound into rubble the proud
capital cities of the world powers. The resulting chaos plunged Earth into a
new Dark Age.</p>
<p>Economies stalled and crashed, countries descended into mayhem and war,
famine was widespread and the situation was exacerbated by the accidental
launch of a handful of nuclear missiles from various locations around the globe.
Fighting against war, famine, long-forgotten diseases, radioactive hotspots and
economic ruin, it took almost ten years for a new order to emerge, one ruled by
the iron-fisted influence of the new age Corporations.</p>
<p>Gradually order returned to the world under the control of the Corporations.
But a fatal shortage of food and raw resources was the only stumbling block to
their domination of the planet. Research into space travel was resurrected. In
the New Age economies, private companies and entrepreneurs invested funds into
the research and slowly a gradual process of integration and alliances gave
birth to the first Clans, great organisations that encompassed Corporations,
their allies and entire populations. The dawning of the first Warp Engine Drive,
combining old Ion Propulsion Engines with experimental Wormhole technology,
acted as a catalyst to speed up the development of spaceships capable of
instantaneous interstellar travel.</p>
<p>Earth became cluttered with dozens of orbiting Manufacturing Stations
churning out advanced spacecraft to serve the needs of the Clans. Freighters,
Battleships, Harvester ships for mining, Colony ships and single-pilot Fighters
were built as well as a host of other exotic starships. Rivalry and competition
resulted in spates of sabotage and industrial espionage as the Clans fought for
supremacy of the new technologies that were being developed day by day.</p>
<p>In 2123, the first interstellar starships left the Sol system and expanded
the reach of the Clans to other star systems. These early exploratory missions
were to act as the platform from which to launch an all out assault on the
