whether from meteorites or planets. Aliens were encountered but the tables had
turned: our fleets of advanced spacecraft were able to repel many of the
attacks. The alien species were many. New alliances and peace treaties were
signed but the aggressiveness and savagery of other extraterrestrial species
led to many attacks and raids.</p>
<p>As mankind continued the expansion, rivalry and competition took a further
step and fuelled wars and arguments between the Clans. Soon all out war was to
ready erupt, Clans allied themselves with others and in time not even the Sol
System was safe from attacks and raids by rival Clan members.</p>
<p>The year is now 2126. The Clans and individuals have agreed that this will
be the year that humanity will at long last colonise the galaxy. A new age has
dawned for mankind, an age of interstellar travel, Clan wars, extraterrestrial
races and alien raids, an age of colonisation and exploration, an age of Solar
Empires.</p>
